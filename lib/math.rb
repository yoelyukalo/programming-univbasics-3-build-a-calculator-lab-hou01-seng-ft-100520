def addition(num1, num2)
  sum = num2 + num1
  sum
end

def subtraction(num1, num2)
  subsum = num1 - num2
  subsum
end

def division(num1, num2)
  divsum = num1 / num2
end

def multiplication(num1, num2)
  mulsum = num1 * num2
  mulsum
end

def modulo(num1, num2)
  num1.modulo(num2)
end

def square_root(num)
  Math.sqrt(num)
end